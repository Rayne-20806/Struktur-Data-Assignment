#include <iostream>
using namespace std;

int main() {
     // 3. Operator c++ artimatika dan logika
     int a;
     int b;
     cout << "Masukkkan angka a: ";
     cin >> a;
     cout << "Masukkan angka b: ";
     cin >> b;
     // operator aritmatika +, -, *, /, %
     cout << "a+b = " << (a+b) << endl;
     cout << "a-b = " << (a-b) << endl;
     cout << "a*b = " << (a*b) << endl;
     cout << "a/b = " << (a/b) << endl; 
     // operator logika >, < >=, <=, ==, !=
     cout << "a > b = " << (a>b) << endl;
}