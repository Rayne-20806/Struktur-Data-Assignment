#include <iostream>
using namespace std;

int main() {
     // 5. Perulangan for, increment dan decrement
     int i;
     int j;
     for (int i = 0; i <= 10; i++) {
          cout << i << "-";
     }
     cout << endl;
     
     for (int j = 20; j >= 10; j--) {
          cout << j << "-";
     }
     cout << endl;
}